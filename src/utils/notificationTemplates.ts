type TemplateCategory =
  | 'fatigue_excellent'
  | 'fatigue_good'
  | 'fatigue_tired'
  | 'fatigue_exhausted'
  | 'sedentary'
  | 'sleep_deficit';

const lastIndices: Record<string, number> = {};

function pickRandom(
  category: TemplateCategory,
  templates: TemplateGenerator[],
  value: number,
): NotificationTemplate {
  let index: number;
  do {
    index = Math.floor(Math.random() * templates.length);
  } while (index === (lastIndices[category] ?? -1) && templates.length > 1);

  lastIndices[category] = index;
  return templates[index](value);
}

